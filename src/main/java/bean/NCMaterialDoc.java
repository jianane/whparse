package bean;

/**
 * @author Jianan
 * @Date
 */
public class NCMaterialDoc {
	String pn;
	String pName;
	boolean isSerial;
	String unit;
	String unitNo;

	public String getPn() {
		return pn;
	}

	public void setPn(String pn) {
		this.pn = pn;
	}

	public String getpName() {
		return pName;
	}

	public void setpName(String pName) {
		this.pName = pName;
	}

	public boolean isSerial() {
		return isSerial;
	}

	public void setSerial(boolean serial) {
		isSerial = serial;
	}


	public String getUnit() {
		return unit;
	}

	public void setUnit(String unit) {
		this.unit = unit;
	}

	public String getUnitNo() {
		return unitNo;
	}

	public void setUnitNo(String unitNo) {
		this.unitNo = unitNo;
	}
}
